package bco.credito.entidad;

public class CEConversionTasa {
    private double mTea;
    private int  mFrecuencia;
    private int mBaseCalculo;

    /**
     * @return the mTea
     */
    public double getmTea() {
        return mTea;
    }

    /**
     * @param mTea the mTea to set
     */
    public void setmTea(double mTea) {
        this.mTea = mTea;
    }

    /**
     * @return the mFrecuencia
     */
    public int getmFrecuencia() {
        return mFrecuencia;
    }

    /**
     * @param mFrecuencia the mFrecuencia to set
     */
    public void setmFrecuencia(int mFrecuencia) {
        this.mFrecuencia = mFrecuencia;
    }

    /**
     * @return the mBaseCalculo
     */
    public int getmBaseCalculo() {
        return mBaseCalculo;
    }

    /**
     * @param mBaseCalculo the mBaseCalculo to set
     */
    public void setmBaseCalculo(int mBaseCalculo) {
        this.mBaseCalculo = mBaseCalculo;
    }

 
}
