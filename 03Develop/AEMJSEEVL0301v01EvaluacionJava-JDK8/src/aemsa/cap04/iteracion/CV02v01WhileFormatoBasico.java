public class CV02v01WhileFormatoBasico {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int lIntN = 10;

        while (lIntN > 0){
            System.out.println("tick " + lIntN);
            lIntN --;
        }
    }

}
