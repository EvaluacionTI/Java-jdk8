package aemsa.cap02.operador;

public class CV06v01OperadorRelacional {

    public static void main(String[] args) {

        // 1.0 Declaracion e inicializacion
        int mValorUno = 1;
        int mValorDos = 2;

        // 3.0 Resultado
        System.out.println("Mayor que :" + mValorDos + " > " + mValorUno);
        System.out.println("Menor que :" + mValorUno + " < " + mValorDos);
        System.out.println("Mayor o igual que :" + mValorDos + " >=  " + mValorUno);
        System.out.println("Menor o igual que :" + mValorUno + " <=  " + mValorDos);
        System.out.println("Igual a :" + mValorDos + " ==  " + mValorDos);
        System.out.println("Diferente a :" + mValorDos + " !=  " + mValorUno);
    }
}
