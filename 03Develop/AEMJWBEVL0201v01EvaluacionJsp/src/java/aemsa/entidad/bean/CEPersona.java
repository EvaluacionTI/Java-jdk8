
package aemsa.entidad.bean;

public class CEPersona {
    
    private int miId;
    private String msNombre;
    private String msCiudad;

    public int getMiId() {
        return miId;
    }

    public void setMiId(int miId) {
        this.miId = miId;
    }

    public String getMsNombre() {
        return msNombre;
    }

    public void setMsNombre(String msNombre) {
        this.msNombre = msNombre;
    }

    public String getMsCiudad() {
        return msCiudad;
    }

    public void setMsCiudad(String msCiudad) {
        this.msCiudad = msCiudad;
    }

}
