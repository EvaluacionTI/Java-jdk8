package aemsa.entidad;

/**
 *
 * @author AEMSA
 */
public class CEPersona {

    private String mNombre;
    private String mCiudad;
    private int mId;

    /**
     * @return the mNombre
     */
    public String getmNombre() {
        return mNombre;
    }

    /**
     * @param mNombre the mNombre to set
     */
    public void setmNombre(String mNombre) {
        this.mNombre = mNombre;
    }

    /**
     * @return the mCiudad
     */
    public String getmCiudad() {
        return mCiudad;
    }

    /**
     * @param mCiudad the mCiudad to set
     */
    public void setmCiudad(String mCiudad) {
        this.mCiudad = mCiudad;
    }

    /**
     * @return the mId
     */
    public int getmId() {
        return mId;
    }

    /**
     * @param mId the mId to set
     */
    public void setmId(int mId) {
        this.mId = mId;
    }

}
