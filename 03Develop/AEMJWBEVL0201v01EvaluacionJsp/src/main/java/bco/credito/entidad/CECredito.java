package bco.credito.entidad;

public class CECredito {
    private double mMontoCredito;
    private double mTea;
    private double mNumeroCuotas;

    /**
     * @return the mMontoCredito
     */
    public double getmMontoCredito() {
        return mMontoCredito;
    }

    /**
     * @param mMontoCredito the mMontoCredito to set
     */
    public void setmMontoCredito(double mMontoCredito) {
        this.mMontoCredito = mMontoCredito;
    }

    /**
     * @return the mTea
     */
    public double getmTea() {
        return mTea;
    }

    /**
     * @param mTea the mTea to set
     */
    public void setmTea(double mTea) {
        this.mTea = mTea;
    }

    /**
     * @return the mNumeroCuotas
     */
    public double getmNumeroCuotas() {
        return mNumeroCuotas;
    }

    /**
     * @param mNumeroCuotas the mNumeroCuotas to set
     */
    public void setmNumeroCuotas(double mNumeroCuotas) {
        this.mNumeroCuotas = mNumeroCuotas;
    }
    
}
